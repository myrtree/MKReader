static Uint32 *UTF8_to_UTF32(Uint32 *UTF32, const char *utf8, int len)
{
	int i, j;
	Uint32 ch;

	for ( i=0, j=0; i < len; ++i, ++j ) {
		ch = ((const unsigned char *)utf8)[i];
		if ( ch >= 0xFC ) {
		    ch  =  (Uint32)(utf8[i]&0x01) << 30;
		    ch |=  (Uint32)(utf8[++i]&0x3F) << 24;
		    ch |=  (Uint32)(utf8[++i]&0x3F) << 18;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 12;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 6;
			ch |=  (Uint32)(utf8[++i]&0x3F);
		} else
		if ( ch >= 0xF8 ) {
		    ch  =  (Uint32)(utf8[i]&0x03) << 24;
		    ch |=  (Uint32)(utf8[++i]&0x3F) << 18;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 12;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 6;
			ch |=  (Uint32)(utf8[++i]&0x3F);
		} else
		if ( ch >= 0xF0 ) {
			ch  =  (Uint32)(utf8[i]&0x07) << 18;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 12;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 6;
			ch |=  (Uint32)(utf8[++i]&0x3F);
		} else
		if ( ch >= 0xE0 ) {
			ch  =  (Uint32)(utf8[i]&0x0F) << 12;
			ch |=  (Uint32)(utf8[++i]&0x3F) << 6;
			ch |=  (Uint32)(utf8[++i]&0x3F);
		} else
		if ( ch >= 0xC0 ) {
			ch  =  (Uint32)(utf8[i]&0x1F) << 6;
			ch |=  (Uint32)(utf8[++i]&0x3F);
		}
		UTF32[j] = ch;
	}
	UTF32[j] = 0;

	return UTF32;
}

